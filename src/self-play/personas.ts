/**
 * Candidate-side personas for self-play. Each archetype is a believable
 * recruiting-domain prospect with goals, anxieties, and a likely
 * conversion path. The candidate-LLM is steered by the persona's
 * `systemPrompt` to behave consistently across turns; the orchestrator
 * pairs them against a salesperson Style and lets a judge LLM call the
 * outcome.
 *
 * These prompts are research-flavoured but written to be obviously
 * SIMULATIONS — we never claim the underlying minors / scams / etc.
 * The recruiting domain itself (foreign work for adults via legal
 * agency contracts) is the same as production.
 */

export interface CandidatePersona {
  slug: string;
  displayName: string;
  /** One-line summary used in the leaderboard / logs. */
  summary: string;
  /**
   * System prompt for the candidate-LLM. Should produce inputs in the
   * same style real candidates use on Telegram — short, casual, often
   * questioning, typos OK. Must NOT break character mid-conversation.
   */
  systemPrompt: string;
  /** First message the candidate sends — sets the opening for the bot. */
  opener: string;
  /**
   * Hint to the judge: given THIS persona, what counts as a "won"
   * outcome? Tunes the judge for the persona's bar.
   */
  judgingHint: string;
}

